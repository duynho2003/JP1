    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // 1. Doc username
        String sUser = txtUser.getText().trim();
        String sPass = txtPassword.getText().trim();
        //2.  Kiem tra username isEmpty?
        if(sUser.isBlank()){
            //3. Bao loi
            JOptionPane.showMessageDialog(null, "Username cannot let blank!");
            txtUser.requestFocus();
            //4. Retun false
            return;
        }
        if(sPass.isBlank()){
            //3. Bao loi
            JOptionPane.showMessageDialog(null, "Password cannot let blank!");
            txtPassword.requestFocus();
            //4. Return false  
            return;
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

